CREATE TABLE MAJOR(
	mCode char(6) PRIMARY KEY,
	name varchar
	);



CREATE TABLE STUDENT(
	sId int PRIMARY KEY,
	firstName varchar,
	lastName varchar,
	mCode char(6) NOT NULL REFERENCES MAJOR ON DELETE RESTRICT ON UPDATE CASCADE,
	pointsEarned smallint DEFAULT 0
	);

CREATE TABLE COURSE(
	cId char(7) PRIMARY KEY,
	title varchar,
	points smallint DEFAULT 0
);

CREATE TABLE RESULT(
	sId int REFERENCES STUDENT ON DELETE CASCADE ON UPDATE CASCADE,
	cId char(7) REFERENCES COURSE ON DELETE RESTRICT ON UPDATE CASCADE,
	year smallint,
	grade varchar(2),
	PRIMARY KEY (sId, cId, year)
	);

CREATE TABLE GRADUATE(
	sId int PRIMARY KEY REFERENCES STUDENT,
	graduationDate date	
	);

